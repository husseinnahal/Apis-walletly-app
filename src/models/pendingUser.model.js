import mongoose from 'mongoose';

const pendingUserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    phone: {
      type: String,
      required: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    otp: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
      expires: 600, // Documents automatically expire and delete after 10 minutes (600 seconds)
    },
  }
);

const PendingUser = mongoose.model('PendingUser', pendingUserSchema);

export default PendingUser;
